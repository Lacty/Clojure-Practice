
; REDUCE
; リストに関数をあたえて畳み込む
; 日本語ってむずかしいね


(reduce + '(1 2 3 4))   ; -> 10
(+ (+ (+ (+ 1) 2) 3) 4) ; -> 10


(reduce + 3 '(1 2 3)) ; -> 9
(+ (+ (+ 3 1) 2) 3)   ; -> 9


(reduce str '("L" "a" "c" "t" "y")) ; -> "Lacty"


; conj consの逆でリストに値(可変長引数)を追加する
; リストだと頭に追加
(conj '(1 2 3) 4) ; -> (4 1 2 3)

(reduce conj '(1 2 3) '(4 5 6)) ; -> (6 5 4 1 2 3)


; ベクタだとケツに追加
(conj [1 2 3] 4) ; -> [1 2 3 4]

(reduce conj [1 2 3] [5 6 7]) ; -> [1 2 3 5 6 7]
